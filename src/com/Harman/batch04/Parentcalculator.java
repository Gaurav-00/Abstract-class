//inheritance is fetching property from one class to other class
//apply inheritance in java

package com.Harman.batch04;

public class Parentcalculator
{                                     //3 properties of parent class as Multiply,add,substract.
    public int Multiply(int x,int y) {
        return x*y;

    }
    public int Addition(int x,int y) {
        return x+y;

    }
    public int Substraction(int x,int y) {
        return x-y;

    }
}
